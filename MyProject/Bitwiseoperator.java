package MyProject;

public class Bitwiseoperator {

	public static void main(String[] args) {
		
		int a = 4 | 8;	// bit wise or	
		System.out.println(a);
		
		int b = 4 & 8;		// bit wise &
		System.out.println(b);
		
		// Ternary operator
				
		int d = 15;
		
		int s = (d <= 10)? 20: 50;  // ternary
		
		if (d < 10) {  s = 20;}
		else {s = 50;}
		
		// // // // //
		
		
	}




	}


